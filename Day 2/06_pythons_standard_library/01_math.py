import math

print("Pi:", math.pi)
print("e:", math.e)

# Logarithms
print("log2(15):", math.log2(15))
print("log10(15):", math.log10(15))
print("log4(15):", math.log(15,4))

# Trigonometric -> input is in radians
print("sin(0.3): ", math.sin(0.3))
print("arc-tan(0.5): ", math.atan(0.5))

# Radians to degrees:
print(math.degrees(1.8))

# Degrees to radians:
print(math.radians(103.1))


